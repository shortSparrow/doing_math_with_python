x = Symbol('x')
fx = Derivative((x**3 + x**2 + x) * (x**2 + x))
print(fx.doit()) # (2*x + 1)*(x**3 + x**2 + x) + (x**2 + x)*(3*x**2 + 2*x + 1)
print(fx.doit().subs({x:3})) # 681



# Також можна рахувати часткову похідну (коли функція залежить від кількох параметрів)
# f(x,y) = 2xy + xy^2  - функція одночасно залежить від x та y, тож візьмемо часткову похідну по x 
# ∂f/∂x = 2y + y^2
x1 = Symbol('x')
y1 = Symbol('y')
fx1 = Derivative(2*x1*y1 + x1*y1*y1, x1) # x1 тут вказує по чому брати похідну, можна було би вказати y1 якби нам треба було по цьому параметру
print(fx1.doit()) # y**2 + 2*y - це похідна по x