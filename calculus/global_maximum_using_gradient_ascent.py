theta_max = find_max_theta(fx, theta)
print('Theta: {0}'.format(math.degrees(theta_max)))            # Theta: 44.999999918501246
print('Maximum Range: {0}'.format(fx.subs({theta:theta_max}))) # Maximum Range: 255.102040816327


