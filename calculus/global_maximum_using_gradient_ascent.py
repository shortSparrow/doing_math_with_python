def gradient_ascent(fx_derivative, x, x0):
  # Перевіряємо чи вираз fx_derivative=0 має розв'язок, тобто чи можна похідну прирівняти до 0
  if not solve(fx_derivative):
      print('Cannot continue, solution for {0}=0 does not exist'.format(fx_derivative))
      return

  epsilon = 1e-6
  step_size = 1e-3
  x_old = x0
  x_new = x_old + step_size*fx_derivative.subs({x: x_old}).evalf()

  # з нашим s_x прикладом буде лише 5 ітерацій, а от для cos буде 2303 ітерацій із таким step_size 
  # (або треба змінити початкову точку x0, щоб зменшити кількість ітерацій) 
  # 
  # Якби fx була б лінійною то частка приросту step_size*fx.subs({x: x_old}).evalf() завжди була б однаковою 
  # (бо тоді похідна була б константою), але оскільки може бути не лінійна функція то і частки приросту 
  # можуть бути різні, якщо проходимо відтинок  з більшою кривизною то частка буд більшою бо похідна 
  # (зростання функції по відношенню до аргументу) більша, а якщо відтинок з меншою кривизною то і приріст буде менший
  while(abs(x_old - x_new) > epsilon):
    x_old = x_new
    x_new = x_old + step_size*(fx_derivative.subs({x: x_old}).evalf())

  return x_new



def find_max_theta(fx, theta):
  # Calculate the first derivative
  d = Derivative(fx, theta).doit()
  theta0 = 1e-3
  theta_max = gradient_ascent(d, theta, theta0)
  return theta_max

theta = Symbol('theta')
