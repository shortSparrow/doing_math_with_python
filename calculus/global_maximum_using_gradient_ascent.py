# 
# 
# Про важливість Step Size та Epsilon
# 
# θ_new = θ_old + λ(dR/dθ)
# λ - це step size і він повинен бути достатньо маленьким, бо якщо буде великим ми можемо проскочити
# вершину (попередня ітерація зупинилася майже біля вершини, але наступна її перескочила), 
