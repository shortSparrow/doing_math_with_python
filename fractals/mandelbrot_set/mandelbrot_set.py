# Задача
# Згенерувати множину Мандельброта 
# 
# Будемо вважати, що наш площина x-y  лежить між точками (тобто обмежена ними) (−2.5, −1.0) та (1.0, 1.0)
# Розглянемо приклад з деталізацією 400 точок по x та y { (x₁, y₁), (x₂, y₂) ... (x₄₀₀, y₄₀₀) } 
# Щоб заповнити усю площину нам потрібні всі можливі пари цих точок, і для цього скористаємося Декартовим добутком
# (Cartesian product) який нам дасть 1600 різних пар
# 
# Розглянемо на прикладі - [(0,0), (1,1), (2,2)] Декартовий добуток буде 3x3=9 різних пар
# це дасть нам такі точки { (0,0), (0,1), (0,2), (1,0), (1,1), (1,2), (2,0), (2,1), (2,2) }
# Якщо ми нанесемо точки на координатну сітку то побачимо що ми намалювали площину квадрата зі сторонами 3x3
# А якщо використати функцію imshow то це створить нам картинку квадрата зі сторонами 3x3 та деталізацією у 9 пікселів
