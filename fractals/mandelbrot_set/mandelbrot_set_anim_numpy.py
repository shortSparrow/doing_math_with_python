import matplotlib.pyplot as plt
import matplotlib.cm as cm
import numpy as np
from matplotlib.animation import FuncAnimation

# Цей код робить те саме що і ./fractals/mandelbrot_set/mandelbrot_set_anim.py, але за допомогою numpy щоб було швидше
# Цей код згенерує дещо іншу картинку ніж ./fractals/mandelbrot_set/mandelbrot_set_anim.py навіть, якщо
# задати однакові параметри resolution та max_iteration. Це тому що тут для точок що не виходять за межі
# фігури стоїть мінімальні значення - 0, а там навпаки, максимальне, бо вони протрималися всю max_iteration
# і досі лишаються в межах. Тож що зображення були однаковими треба тут змінити щоб точки що не виходять
# за межу мали максимальне значення, тобто замість 
# divergence_map = np.zeros(C.shape, dtype=float) поставити
# divergence_map = np.full(C.shape, MAX_ITER_FINAL, dtype=float)


# --- Налаштування ---
# Область малювання (x від -2.5 до 1.0, y від -1.1 до 1.1 для симетрії)
x_min, x_max = -2.5, 1.0
y_min, y_max = -1.1, 1.1

# Роздільна здатність (кількість точок по одній осі).
# Для анімації краще взяти менше значення (наприклад, 200-300), щоб вона рендерилася швидше.
RESOLUTION = 300

# Максимальна кількість ітерацій, яку ми досягнемо в кінці анімації
MAX_ITER_FINAL = 100

# Створюємо сітку значень (рівномірно розподіляємо RESOLUTION точок між min та max)
ls_x = np.linspace(x_min, x_max, RESOLUTION)
ls_y = np.linspace(y_min, y_max, RESOLUTION)

# .meshgrid() перетворює два одновимірні масиви (координати по осях x та y) у дві двовимірні матриці, 
# які представляють собою сітку координат для кожної точки зображення.
# Нехай є сітка 3x3 
# x = [1, 2, 3] (координати стовпців)
# y = [10, 20, 30] (координати рядків)
# Коли викликаємо X, Y = np.meshgrid(x, y), то отримуємо:
#   Матриця X (координати по горизонталі):
#   Вона дублює рядок x стільки разів, скільки елементів у y.
#   [[1, 2, 3],
#    [1, 2, 3],
#    [1, 2, 3]]
# 
#   Матриця Y (координати по вертикалі):
#   Вона дублює стовпчик y стільки разів, скільки елементів у x.
#   [[10, 10, 10],
#    [20, 20, 20],
#    [30, 30, 30]]
# 
# У нашому коді з множиною Мандельброта кожен піксель — це комплексне число c = x + yi.
# Щоб не писати два вкладені цикли for x in ... та for y in ... (що в Python працює повільно), 
# ми просто додаємо ці дві матриці: C = X + 1j * Y
# В результаті ми отримуємо одну велику матрицю C того ж розміру, де в кожній клітинці вже лежить 
# готове комплексне число для цієї конкретної точки екрана. Це дозволяє NumPy виконувати обчислення 
# над усіма 160 000 пікселями (400x400) одночасно, що в десятки разів швидше за звичайний цикл.
re, im = np.meshgrid(ls_x, ls_y, indexing='ij')
C = re + 1j * im


# np.zeros() створює новий масив, який заповнений нулями, але має точно такий самий розмір (shape) 
# як і масив C і dtype float
divergence_map = np.zeros(C.shape, dtype=float)

# Масив 'z' для обчислень (z = z^2 + c), спочатку всі z = 0
Z = np.zeros(C.shape, dtype=complex)

# Масив масок: True означає, що точка ще НЕ "втекла" (модуль < 2)
not_escaped = np.ones(C.shape, dtype=bool)


# --- Налаштування графіку ---
fig, ax = plt.subplots(figsize=(10, 7))
ax.set_title("Анімація побудови множини Мандельброта")
# Приховуємо осі для гарного вигляду
ax.axis('off')

# Початкове відображення порожнього або нульового масиву
# ВАЖЛИВО: Щоб imshow працювала в анімації, initial data має бути float np.array
# origin='lower', щоб y-вісь йшла знизу вгору
img = ax.imshow(divergence_map.T, cmap=cm.magma, origin='lower',
                extent=(x_min, x_max, y_min, y_max),
                animated=True, interpolation='nearest')

# Додаємо колірну шкалу
plt.colorbar(img, ax=ax, label='Кількість ітерацій')


# --- Функція оновлення для анімації ---
def update(frame):
    """
    Ця функція викликається для кожного кадру.
    Вона виконує ОДНУ ітерацію z = z^2 + c для ВСІХ точок одночасно.
    """
    global Z, not_escaped, divergence_map

    # Поточна ітерація (frame починається з 0, тому +1)
    current_iter = frame + 1

    # Працюємо тільки з тими точками, які ще не втекли
    # 1. Виконуємо крок алгоритму: z = z^2 + c
    Z[not_escaped] = Z[not_escaped]**2 + C[not_escaped]

    # 2. Знаходимо точки, які щойно втекли на цьому кроці (модуль > 2)
    just_escaped = (np.abs(Z) >= 2.0) & not_escaped

    # 3. Записуємо номер поточної ітерації для цих точок
    divergence_map[just_escaped] = current_iter

    # 4. Оновлюємо маску: виключаємо точки, які втекли, з подальших обчислень
    not_escaped[just_escaped] = False

    # --- Оновлення графіку ---
    # Оновлюємо дані зображення. Транспонуємо (.T), щоб збігалося з осями x, y
    img.set_array(divergence_map.T)

    # Динамічно оновлюємо межі колірної шкали, щоб картинка була соковитою
    # Точки, що не втекли, мають 0, тому беремо макс. значення серед тих, що втекли.
    v_max = current_iter
    img.set_clim(vmin=0, vmax=v_max)

    return img,

# --- Створення анімації ---
# frames: кількість кадрів дорівнює максимальній кількості ітерацій
# interval: затримка між кадрами в мілісекундах
ani = FuncAnimation(fig, update, frames=MAX_ITER_FINAL, interval=50, blit=True, repeat=False)

plt.show()