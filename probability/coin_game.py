# імовірністю видавати решту банкнотою певного номіналу. Припустимо я маю банкноти [5, 10, 20, 50]
# і я хочу щоб 5 і 10 видавалися рідше ніж 20 та 50
# імовірність випадання 5 чи 10 разом така сама, як і імовірність 20 або 50 окремо

def run_coin_game_many_times():
  dollar_bills = [5, 10, 20, 50] 
  probability = [1/6, 1/6, 1/3, 2/3]

  stats = {5: 0, 10: 0, 20: 0, 50: 0}
